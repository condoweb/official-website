                <h3>Cos'è un condominio?</h3>
                <p>
                    Il condominio è un tipo di comunione prevista dall'ordinamento giuridico italiano.
                    <br />
                    Si ha quando in un edificio con più unità immobiliari più persone sono ciascuna proprietaria di parte di esse in via esclusiva: queste persone sono comproprietarie delle parti comuni (o condomini).
                    <br />
                    Più semplicemente il termine è utilizzato per definire un edificio suddiviso in appartamenti.
                    <br /><br />
                    Già nella storia, con il diritto romano, si riconosceva una proprietà privata ereditata dal genitore e rimasta indivisa tra i fratelli (consortium). In età classica si sviluppò il concetto di proprietà comune di divisione "pro quota" con ripartizione proporzionale dei diritti e degli obblighi con forme di tutela analoghe al diritto moderno (rei vindicatio partiaria).
                    <br />
                    Il condominio è disciplinato dal codice civile ma questo non ne da una specifica definizione, sopratutto se si è dei profani in merito, per via della natura "umana" e "fisica" al quale è collegato implicitamente. 
                    <br />
                    Il condominio è un concetto che può essere spiegato per vari settori (ad esempio legislativo, fisico od altro) ma per renderlo semplice basti pensare al concetto di "comune convinvenza" e di "civiltà"... o "collettività".
                    <br />
                    In sintesi, per comprende al volo la cosa, il condomio è un'insieme di persone e cose che "convivono" sotto un unico complesso edile (casa fatta di più case), piccolo o grande che sia, le cui attitudini educative sono regolamente seguendo le leggi previste.
                </p>

                <h3>A cosa serve il condominio?</h3>
                <p>
                    Per essere espliciti, il codondominio serve a mantenere, organizzare, e soddisfare i bisogni di una comune, tranquilla, convivenza di un gruppo di persone che abitano nello stesso complesso edile e dunque a stretto contatto.
                </p>

                <h3>Chi gestisce il condominio?</h3>
                <p>
                    Il condominio è una realtà anche per la legge, non solo fisica, quindi è gestito da organi specifici, tutelati e regolamentati, dal codice civile.
                    <br />
                    Gli organi attraverso i quali il condominio lavora sono: l'amministratore; l'assemblea e, dove previsto, il consiglio.
                    <br />
                    La gestione di un condominio è dunque affidata ad un "amministratore di condominio", il quale è incaricato di concordare assemblee, attenuare e regolamentare, informare e divulgare il materiale necessario a tutti i "condomini", per via cartacea e -seguendo le nuove normative di legge vigenti- obbligatoriamente per vie telematiche (sito web). 
                    <br />
                    L'amministratore condominiale è paragonabile ad un ente giuridico.
                    <br />
                    Anche l'assemblea è cosiderata parte integrante del meccanismo. Per la legge, quest'ultima è l'organo deliberante del condominio e ne rappresenta le volontà interne.
                    <br />
                    In ultimo vi è il "consiglio di condominio". Si tratta di un organo facoltativo, interno al condominio stesso, con funzioni consultive e di controllo, che, ai sensi di legge, può essere nominato negli edifici con almeno 12 unità immobiliari.
                </p>
                
